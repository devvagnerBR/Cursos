
// Objetos Globais 

let lista = Array("A", "B", "C");
console.log(lista); // ["A", "B", "C"]

let pessoa = Object();
console.log(pessoa); // {}

let a = Number("42");
console.log(a); // 42:Number

let b = String(60);
console.log(b); // 60:String

const inteiro = Number.isInteger(2.5);
console.log(inteiro); // false

const dia = Date();
console.log(dia); // Sun Aug 21 2022 15:11:26 GMT-0300 (Horário Padrão de Brasília)

const matematica = Math.pow(5, 2);
console.log(matematica); // PotÊncia 5² = 25

const max = Math.max(25, 36, 45, 88);
console.log(max); // 88

const random = Math.random();
console.log(random); // random Number

const raizQuadrada = Math.sqrt(100).toFixed(1);
console.log(raizQuadrada); // 10.0
